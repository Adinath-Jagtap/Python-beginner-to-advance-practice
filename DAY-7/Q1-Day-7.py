# Q7 Create class with attributes and print them

# Created Class with attributes
class Car:
    brand = "Toyota"
    wheels = 4
    doors = 4

#printing the attributes using "class".
print(Car.brand)
print(Car.wheels)
print(Car.doors)

#creating an object of class Car
car1 = Car()

#printing the attributes using the object creaed using Car class.
print(car1.brand)
print(car1.wheels)
print(car1.doors)